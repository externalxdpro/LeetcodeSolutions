int minOperations(std::vector<int> &nums) {
    std::unordered_map<int, int> occurences;

    for (int i : nums) {
        occurences[i]++;
    }

    int ops = 0;
    for (auto [num, occ] : occurences) {
        if (occ == 1) {
            return -1;
        } else if (occ % 3 == 1) {
            ops += occ / 3 - 1;
            ops += 2;
        } else {
            ops += occ / 3;
            ops += (occ % 3) / 2;
        }
    }

    return ops;
}

